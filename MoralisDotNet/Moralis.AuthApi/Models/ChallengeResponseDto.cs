using Newtonsoft.Json;
using System.Runtime.Serialization;

namespace Moralis.AuthApi.Models
{
    /// <summary>
    /// Respopnse returned by the Challenge operation.
    /// </summary>
    public class ChallengeResponseDto
    {
        /// <summary>
        /// Internal identifier
        /// </summary>
        [DataMember(Name = "id", EmitDefaultValue = false)]
        [JsonProperty(PropertyName = "id")]
        [System.Text.Json.Serialization.JsonPropertyName("id")]
        public string Id { get; set; }

        /// <summary>
        /// Message meant to be signed.
        /// </summary>
        [DataMember(Name = "message", EmitDefaultValue = false)]
        [JsonProperty(PropertyName = "message")]
        [System.Text.Json.Serialization.JsonPropertyName("message")]
        public string Message { get; set; }

        /// <summary>
        /// Profile Id generated by the Moralis Authentication API.
        /// </summary>
        [DataMember(Name = "profileId", EmitDefaultValue = false)]
        [JsonProperty(PropertyName = "profileId")]
        [System.Text.Json.Serialization.JsonPropertyName("profileId")]
        public string ProfileId { get; set; }
    }
}
